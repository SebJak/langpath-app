package com.langpath.app.model.response;

public class AxisValue {

    private long x; //how long ago
    private long y; // how much repeats
    private long z; // how many words

    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    public long getY() {
        return y;
    }

    public void setY(long y) {
        this.y = y;
    }

    public long getZ() {
        return z;
    }

    public void setZ(long z) {
        this.z = z;
    }
}
